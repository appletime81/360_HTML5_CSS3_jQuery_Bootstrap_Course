// 在主控台打印 Hello
// const 常數:  不可重新賦予新值
// let 變數: 可以重新賦予新值

// condition 1: const
const userName1 = "Frank"
console.log("Hello " + userName1)
console.log("Hi " + userName1)
console.log(userName1 + "，你好嗎?")

// condition 2: let
let userName2 = "Frank"
console.log("Hello " + userName2)
userName2 = "Andy"
console.log("Hi " + userName2)
console.log(userName2 + "，你好嗎?")